            <p>
                Pensado para festivais o Muraw-flerte é a feitura de um mural composto de perguntas acerca das danças assistidas e das reverberações do festival nos espaços públicos. As perguntas aqui são uma forma de aproximação, uma maneira de, nós da TransaKrytica e o público, flertar com os espetáculos e com os artistas presentes no festival. Os espectadores serão convidados a fazer suas perguntas ao final de cada apresentação. As kryticas-articuladorxs TransaKrytica vão, com as perguntas, compor o muraw-flerte produzido com lambe-lambes manufaturados diariamente. O intuito, também, é que o muraw-flerte resulte em uma obra plástica que ficará exposta. A vontade dessa obra é o flerte que ela pode provocar.                      
            </p>
            <p>
                Lambe-lambe o festival é a ação de kolagem de lambe-lambes pelos caminhos promovidos por um festival: as idas e vindas às apresentações pela cidade, aos restaurantes, pousadas e hotéis geram percursos onde os lambe-lambes com o #texto-presença serão kolados em postes ou muros, tornando-se rastros da TransaKrytica em seu enkontro com o festival. 
