              <strong>Joubert Arrais</strong> é dançarino e crítico de dança. Cearense-alagoano atualmente residente em Juazeiro do Norte/CE, onde atua como professor adjunto do bacharelado em Jornalismo da Universidade Federal do Cariri - UFCA. Atuou até janeiro de 2017 como professor adjunto no curso de bacharelado e licenciatura em Dança da Fap/Unespar. Escreve no enquantodancas.net, site vinculado às ações escritas do seu projeto itinerante, o "Crítica com a Dança", que resultou no livro Dança com a Crítica (Fortaleza: Expressão Gráfica, 2013), ambos com apoio do Prêmio Funarte de Dança Klauss Vianna 2011, em ação conjunta com o projeto Crítica de Dança – Contextos Nordestinos, via patrocínio do Programa de Cultura Banco do Nordeste/BNDES 2011. Com doutorado em Comunicação e Semiótica (PUCSP, 2015), é também mestre em Dança (UFBA, 2008) e bacharel em Comunicação Social/Jornalimo(UFC, 2003), com formação artística pelo cem - centro em movimento (Lisboa, 2009-2011). Como criador, trabalha com solos autorais de Dança com Performance, como o espetáculo Eu danço Sambarroxé (2007-2011), a performance Virar Bicho (2010-11) e o experimento dançado Projeto Masculina_A senhora tá tão masculina!(2013-atual). Em 2010 participou do Programa Colaboratorio como artista residente em Teresina / PI, iniciativa do Festival Panorama - RJ em parceria com o Núcleo Dirceu - PI. 
