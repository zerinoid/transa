        <p>Instalação Lowcuradoria (2017)</p>
    </div>  
    <div class="black-box">
        <div class="texto">
            <p>
                É uma instalação-encruzilhada: um ambiente sinestésico criado com materiais elementares, tecnológicos e multimidiáticos. Ação-instalação pensada, no intuito de, na relação corpo-ambiente, sutilmente desbloquear os sentidos. Sendo site-specific, é feita a partir do lugar onde se instala, logo, são coletados plantas e materiais nas matas dos arredores de onde acontece e, na montagem são usadas técnicas de "armengue", ou seja, procedimentos orientados pelo conhecimento intuitivo.                        
            </p>
            <p>
                A instalação acontece também na camada sonora, durante os dias instalativos serão captados áudios-depoimentos do público participante, esses serão mixados com sons  de nebulosas, meteoros e foguetes (fonte: NASA), mais mantras, pontos afroameríndios e música eletrônica, o que constituirá a ambiência sonora trabalhada pela beatmaker. 
            </p>
            <p>
                Em suma, a lowcuradoria é a feitura de uma encruza cênica, onde cada ponto da seta tenha um lugar de relação e de ativação multisensorial: passagem, limpeza, desabafo, desobstrução de fluxos. E também, é um lugar de mediar juntos um momento de troca-cruzamento das experiências vividas onde daí resulte a co-criação de uma memória (não-linear) que reverbere. 
            </p>
            <p>
                A curadoria, aqui, é entendida como agência de cura, aspirando a prática curatorial como um lugar imaginário, ao mesmo tempo que material-palpável onde se estabeleça relações entre vários seres e saberes, ligados às múltiplas inteligências, de plantas, de pedras, de divindades da natureza e etc. Trata-se da curadoria como caboclamento intermediando as forças humanas, naturais e sobrenaturais.
            </p>
